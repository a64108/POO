# Faça um programa que peça dois números e imprima o maior deles

# n1 - input
# n2 - input
# funçao de comparaçao
# maior - output


n1 = float(input("Escreva o primeiro número:"))
n2 = float(input("Escreva o segundo número:"))

maior = max(n1, n2)

print("O maior número é:", maior)
